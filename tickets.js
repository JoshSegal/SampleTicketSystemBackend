class Tickets {
    constructor(Name, Email, Department, Priority, ContactID, Title, Description, Status) {
        this.Name = Name;
        this.Email = Email;
        this.Department = Department;
        this.Priority = Priority;
        this.ContactID = ContactID;
        this.Title = Title;
        this.Description = Description;
        this.Status = Status;
    }
}
module.exports= Tickets;